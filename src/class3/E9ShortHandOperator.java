package class3;

public class E9ShortHandOperator {
    public static void main(String[] args) {
//Long Hand
        int number=10;
        number=number+20;
        System.out.println(number);

        //Short Hand
        number+=20;

        int num=10;
        num+=90;
        num-=50;
        num*=2;
        num/=10;
        System.out.println(num);






    }
}
