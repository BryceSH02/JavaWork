package class6;

public class E5witchVariable {
    public static void main(String[] args) {

        char gender='F';

        switch (gender) {
            case 'F':
            System.out.println("Female");
            break;
            case 'M':
                System.out.println("Male");
                break;
            case 'O':
                System.out.println("Other");
                break;
        }
    }
}
