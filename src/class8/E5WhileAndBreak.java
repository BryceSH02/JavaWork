package class8;

public class E5WhileAndBreak {
    public static void main(String[] args) {

        int temp=85;
        boolean summer=true;
        while (summer){
            if (temp<100){
                System.out.println("Its good I enjoy the summer");
            }else{
                System.out.println("It is very hot");
                break;
            }
            temp=temp+3;

        }









    }
}
